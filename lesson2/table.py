m=int(input("Введите номер химического элемента: "))
if m==3:
    print('Это литий Li')
elif m==17:
    print(' Это хлор Cl')
elif m==25:
    print('Это магний Mn')
elif m==80:
    print('Это меркурий Hg')
else:
    print('Не знаю, что это, но точно не литий, хлор, магний и не меркурий.')
